package JavaBasic.types.variables;

public class CastingDemo {

    public static void main(String[] args) {

        //Implicit Casting/Automate
        // Byte-Short-Int-Long
        // 1-2-4-8

        short x = 1;
        int y = x + 2;
        //System.out.println(y);


        // Explicit Casting
        double k = 1.5;
        int z = (int) k+2;
        System.out.println(z);




    }
}
